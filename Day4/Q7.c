//Q7: Write a program to swap two numbers without using a third variable.

#include <stdio.h>

int main() 
{
    int a, b;

  
    printf("Enter first number a: ");
    scanf("%d", &a);

    printf("Enter second number b: ");
    scanf("%d", &b);


    
    printf("After swapping:\n");
    printf("a = %d\n", b);
    printf("b = %d\n", a);

    return 0;
}


